'''Vou resumir os conceitos fundamentais da programação orientada a objetos (POO) e o princípio SOLID:

1. **Encapsulamento**:
   - Encapsulamento é o conceito de agrupar os dados (atributos) e os métodos que operam nesses dados em uma única unidade, chamada de classe.
   - Essa unidade controla o acesso aos dados, permitindo que apenas os métodos da classe possam modificar ou acessar seus atributos. Isso garante a segurança e a integridade dos dados.

2. **Herança**:
   - Herança é um mecanismo que permite que uma classe herde atributos e métodos de outra classe (classe pai ou superclasse).
   - As classes que herdam são chamadas de subclasses ou classes filhas.
   - Elas podem estender o comportamento da classe pai adicionando novos métodos ou modificando os métodos existentes.

3. **Polimorfismo**:
   - Polimorfismo significa "muitas formas" e permite que objetos de diferentes classes sejam tratados de maneira uniforme.
   - Isso é alcançado por meio de métodos com o mesmo nome em classes diferentes, mas com implementações específicas para cada classe.
   - Permite que um objeto possa ser referenciado por meio de uma referência de sua classe base, mas o comportamento invocado pode depender da classe real do objeto em tempo de execução.

4. **SOLID**:
   - SOLID é um acrônimo que representa cinco princípios da programação orientada a objetos:
     - **S (Single Responsibility Principle)**: Uma classe deve ter apenas uma razão para mudar.
     - **O (Open/Closed Principle)**: Uma classe deve estar aberta para extensão, mas fechada para modificação.
     - **L (Liskov Substitution Principle)**: Subtipos devem ser substituíveis por seus tipos base.
     - **I (Interface Segregation Principle)**: Muitas interfaces específicas são melhores do que uma interface geral.
     - **D (Dependency Inversion Principle)**: Dependa de abstrações, não de implementações.
   - Esses princípios visam criar código mais modular, flexível, extensível e fácil de manter, promovendo boas práticas de design orientado a objetos.

Esses conceitos formam a base da programação orientada a objetos e são essenciais para a compreensão e o desenvolvimento de sistemas complexos e modularizados.'''